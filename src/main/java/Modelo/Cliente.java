package Modelo;

/**
 *
 * @author thiag
 */
public class Cliente {
    int id;
    String dni;
    String nome;
    String estado;
    String endereco;

    public Cliente() {
    }

    public Cliente(int id, String dni, String nome, String estado, String endereco) {
        this.id = id;
        this.dni = dni;
        this.nome = nome;
        this.estado = estado;
        this.endereco = endereco;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    
    
    
}
